# Free Microsoft Azure Neural Voice (High Quality Hindi Female Voice)
VOICE = "hi-IN-SwaraNeural"  

async def generate():
    print(f"Generating 90 high-quality neural TTS files using {VOICE}...")
    for num in range(1, 91):
        phrase = phrases.get(num, "")
        
        # Format the text natively: "Number 24. 24 Carat!"
        if phrase:
            text = f"नंबर {num}। {phrase}!"
        else:
            text = f"नंबर {num}।"

        output_file = f"public/audio/hi/{num}.mp3"
        
        communicate = edge_tts.Communicate(text, VOICE)
        await communicate.save(output_file)
        
    print("Successfully generated all 90 audio files in public/audio/hi/")

if __name__ == "__main__":
    asyncio.run(generate())
